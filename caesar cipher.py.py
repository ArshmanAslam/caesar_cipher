def program(text , shift , option):
    txt = ''
    if option == 'decode':
        shift *= -1
    for x in text:
        if x in alphabet:
            position = alphabet.index(x)
            new_position = position + shift
            txt += alphabet[new_position]
        else:
            txt += x    
    print(f"The {option}d letter is {txt}")

